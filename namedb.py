def get_all_names():
    """
    Recursively traverses the NameDB class to collect all first and last names.
    """
    first_names = []
    last_names = []
    
    def recurse(obj):
        if hasattr(obj, 'first_names') and isinstance(obj.first_names, list):
            first_names.extend(obj.first_names)
        if hasattr(obj, 'last_names') and isinstance(obj.last_names, list):
            last_names.extend(obj.last_names)
        
        for attr_name in dir(obj):
            if not attr_name.startswith('__'):
                attr = getattr(obj, attr_name)
                if isinstance(attr, type): # Check if it's a class
                    recurse(attr)

    recurse(NameDB)
    return list(set(first_names)), list(set(last_names))

ALL_FIRST_NAMES, ALL_LAST_NAMES = get_all_names()
